
def addNum(number1, number2):
    number = number1 + number2
    return number

def subtractNum(number1, number2):
    number = number1 - number2
    return number

def multiplyNum(number1, number2):
    number = number1 * number2
    return number

def divideNum(number1, number2):
    number = number1 / number2
    return number

